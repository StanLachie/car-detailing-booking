// Booking table for detailing appointments
export const booking = sqliteTable(
  "booking",
  {
    id: text("id").primaryKey(),
    name: text("name").notNull(),
    mobile: text("mobile").notNull(),
    address: text("address").notNull(),
    returningCustomer: integer("returning_customer", { mode: "boolean" })
      .default(false)
      .notNull(),
    vehicleYear: text("vehicle_year").notNull(),
    vehicleMake: text("vehicle_make").notNull(),
    vehicleModel: text("vehicle_model").notNull(),
    serviceType: text("service_type").notNull(), // "both", "interior", "exterior"
    scent: text("scent").notNull(), // "none", "no-preference", or scent name
    specialRequests: text("special_requests"),
    date: text("date").notNull(), // ISO date string YYYY-MM-DD
    timeOfDay: text("time_of_day").notNull(), // "morning" or "afternoon"
    status: text("status").default("pending").notNull(), // pending, confirmed, completed, cancelled
    createdAt: integer("created_at", { mode: "timestamp_ms" })
      .default(sql`(cast(unixepoch('subsecond') * 1000 as integer))`)
      .notNull(),
    updatedAt: integer("updated_at", { mode: "timestamp_ms" })
      .default(sql`(cast(unixepoch('subsecond') * 1000 as integer))`)
      .$onUpdate(() => new Date())
      .notNull(),
  },
  (table) => [
    index("booking_date_idx").on(table.date),
    index("booking_status_idx").on(table.status),
  ]
);

